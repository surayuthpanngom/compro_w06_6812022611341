#include <stdio.h>

int main() {
    int i = 1, j = 2, k;

    // บรรทัดที่ 2: k = 1 + 2 = 3
    k = i + j;

    // บรรทัดที่ 3: i = 1 + (3 * 2) = 1 + 6 = 7
    i = i + (k * j);

    // บรรทัดที่ 4: j = 7 / 2 = 3 (หารแบบจำนวนเต็ม)
    j = i / 2;

    // บรรทัดที่ 5: k = 7 % 2 = 1
    k = i % 2;

    // บรรทัดที่ 6: i = (3 + 1) * 3 = 4 * 3 = 12
    i = (j + k) * 3;

    printf("w06-02-01:\ni = %d, j = %d, k = %d\n\n", i, j, k);
    return 0;
}
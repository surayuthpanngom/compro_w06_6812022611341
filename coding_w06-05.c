#include <stdio.h>

int main() {
    int A, B, C, D, E, F, G;

    // A = -2 + 5 * 2;
    // ลำดับ: 5 * 2 = 10 -> -2 + 10 = 8
    A = -2 + 5 * 2;
    printf("A = %d\n", A);

    // B = 10 / 2 * 3;
    // ลำดับ: 10 / 2 = 5 -> 5 * 3 = 15
    B = 10 / 2 * 3;
    printf("B = %d\n", B);

    // C = 6 / 2 + 3 * (4 % 2);
    // ลำดับ: 4 % 2 = 0 -> 6 / 2 = 3 -> 3 + 3 * 0 = 3
    C = 6 / 2 + 3 * (4 % 2);
    printf("C = %d\n", C);

    // D = (5 + 2) * 15 % 4;
    // ลำดับ: 5 + 2 = 7 -> 7 * 15 = 105 -> 105 % 4 = 1
    D = (5 + 2) * 15 % 4;
    printf("D = %d\n", D);

    // E = 6 + 2 * 2 - 6 / 2;
    // ลำดับ: 2 * 2 = 4, 6 / 2 = 3 -> 6 + 4 - 3 = 7
    E = 6 + 2 * 2 - 6 / 2;
    printf("E = %d\n", E);

    // F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    // ลำดับ: 3 * 2 = 6, 8 / 4 = 2, 6 % 5 = 1 -> 5 + 6 - 2 + 1 = 10
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    printf("F = %d\n", F);

    // G = (4 + 3) * 2 - 10 / (2 + 3);
    // ลำดับ: 4 + 3 = 7, 2 + 3 = 5 -> 7 * 2 = 14, 10 / 5 = 2 -> 14 - 2 = 12
    G = (4 + 3) * 2 - 10 / (2 + 3);
    printf("G = %d\n", G);

    return 0;
}